public class Char02 {
	
	public static void main(String[] args) {
		
		char c1 = 97;
		System.out.println(c1);
		
		char c2 = 'a';
		System.out.println((int)c2);
		char c3 = '杨';
		System.out.println((int)c3);
		System.out.println('a' + 100);
		
		char c4 = 'b' + 1;
		System.out.println((int)c4);
		System.out.println(c4);
	}
}